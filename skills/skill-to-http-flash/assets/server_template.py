def _get_local_ips() -> list:
    """Collect local IPs including loopback."""
    ips = [ipaddress.ip_address("127.0.0.1"), ipaddress.ip_address("::1")]
    seen = {"127.0.0.1", "::1"}
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as s:
            s.connect(("8.8.8.8", 80))
            ip_str = s.getsockname()[0]
            if ip_str and ip_str not in seen:
                ips.append(ipaddress.ip_address(ip_str))
                seen.add(ip_str)
    except Exception:
        pass
    try:
        hostname = socket.gethostname()
        for addr in socket.getaddrinfo(hostname, None):
            ip_str = addr[4][0].split('%')[0]
            if ip_str and ip_str not in seen:
                try:
                    ip = ipaddress.ip_address(ip_str)
                    if not ip.is_loopback:
                        ips.append(ip)
                        seen.add(ip_str)
                except ValueError:
                    pass
    except Exception:
        pass
    return ips


def _cert_is_valid() -> bool:
    if not CERT_FILE.exists() or not KEY_FILE.exists():
        return False
    try:
        from cryptography import x509
        from cryptography.hazmat.backends import default_backend
        cert_pem = CERT_FILE.read_bytes()
        cert = x509.load_pem_x509_certificate(cert_pem, default_backend())
        now = datetime.datetime.now(datetime.timezone.utc)
        return cert.not_valid_before_utc <= now <= cert.not_valid_after_utc
    except Exception:
        return False


def _cert_needs_regeneration() -> bool:
    if not _cert_is_valid():
        return True
    try:
        from cryptography import x509
        from cryptography.hazmat.backends import default_backend
        cert_pem = CERT_FILE.read_bytes()
        cert = x509.load_pem_x509_certificate(cert_pem, default_backend())
        san = cert.extensions.get_extension_for_class(x509.SubjectAlternativeName)
        cert_ips = set(str(ip) for ip in san.value.get_values_for_type(x509.IPAddress))
        current_ips = {str(ip) for ip in _get_local_ips()}
        current_ips.add("127.0.0.1")
        if not current_ips.issubset(cert_ips):
            logger.info("Certificate IPs outdated, regenerating")
            return True
    except Exception:
        return True
    return False


def _generate_cert():
    """Generate self-signed certificate (cryptography lib, with SAN)."""
    if not _cert_needs_regeneration():
        logger.info("Reusing existing valid certificate")
        return

    logger.info("Generating self-signed certificate...")
    from cryptography import x509
    from cryptography.hazmat.primitives import hashes, serialization
    from cryptography.hazmat.primitives.asymmetric import rsa
    from cryptography.hazmat.backends import default_backend
    from cryptography.x509.oid import NameOID

    private_key = rsa.generate_private_key(
        public_exponent=65537, key_size=2048, backend=default_backend()
    )

    subject = issuer = x509.Name([
        x509.NameAttribute(NameOID.COUNTRY_NAME, "CN"),
        x509.NameAttribute(NameOID.ORGANIZATION_NAME, "skill-to-http-flash"),
        x509.NameAttribute(NameOID.COMMON_NAME, "localhost"),
    ])

    san_entries = [
        x509.DNSName("localhost"),
        x509.IPAddress(ipaddress.IPv4Address("127.0.0.1")),
        x509.IPAddress(ipaddress.IPv6Address("::1")),
    ]
    for ip in _get_local_ips():
        san_entries.append(x509.IPAddress(ip))

    cert = (
        x509.CertificateBuilder()
        .subject_name(subject)
        .issuer_name(issuer)
        .public_key(private_key.public_key())
        .serial_number(x509.random_serial_number())
        .not_valid_before(datetime.datetime.now(datetime.timezone.utc))
        .not_valid_after(
            datetime.datetime.now(datetime.timezone.utc)
            + datetime.timedelta(days=CERT_VALIDITY_DAYS)
        )
        .add_extension(x509.SubjectAlternativeName(san_entries), critical=False)
        .sign(private_key, hashes.SHA256(), default_backend())
    )

    _pem_encoding = getattr(serialization, "Encoding").PEM
    KEY_FILE.write_bytes(private_key.private_bytes(
        encoding=_pem_encoding,
        format=serialization.PrivateFormat.PKCS8,
        encryption_algorithm=serialization.NoEncryption(),
    ))
    KEY_FILE.chmod(0o600)
    CERT_FILE.write_bytes(cert.public_bytes(_pem_encoding))
    logger.info(f"Certificate written to {CERT_FILE}")


