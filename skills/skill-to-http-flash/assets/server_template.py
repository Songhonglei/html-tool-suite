def _example_payload() -> dict:
    example = {}
    props = INPUT_SCHEMA.get("properties", {})
    for key, prop in props.items():
        t = prop.get("type", "string")
        if t == "string":
            example[key] = prop.get("default", "example_value")
        elif t in ("number", "integer"):
            example[key] = prop.get("default", 0)
        elif t == "boolean":
            example[key] = prop.get("default", False)
        elif t == "array":
            example[key] = prop.get("default", [])
        elif t == "object":
            example[key] = prop.get("default", {})
    return example


@app.get("/health")
async def health():
    """Probe: entry 文件存在 + interpreter 在 PATH。"""
    entry_ok = False
    entry_reason = ""
    try:
        entry = _resolve_entry()
        entry_ok = entry.exists()
        entry_reason = "ok" if entry_ok else "missing"
    except FlashError as e:
        entry_reason = e.message

    interp = INTERPRETER or "python3"
    interp_ok = shutil.which(interp) is not None

    return {
        "status": "ok" if (entry_ok and interp_ok) else "degraded",
        "skill": SKILL_NAME,
        "uptime_seconds": int(time.time() - _start_time),
        "entry": {
            "path": str(ENTRY_PATH) if ENTRY_PATH else None,
            "ok": entry_ok,
            "reason": entry_reason,
        },
        "interpreter": {
            "name": interp,
            "ok": interp_ok,
        },
    }


