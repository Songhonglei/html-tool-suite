_start_time = time.time()
_bound_port: int = 0


def _get_lan_ips() -> list[str]:
    return [str(ip) for ip in _get_local_ips() if str(ip) not in ("127.0.0.1", "::1")]


def main():
    import argparse

    parser = argparse.ArgumentParser(description=f"{SKILL_NAME} API Server (flash v2.0)")
    parser.add_argument("--port", type=int, default=PORT, help=f"Listen port (default: {PORT})")
    parser.add_argument("--host", type=str, default="0.0.0.0", help="Listen host")
    parser.add_argument(
        "action", nargs="?", default="start",
        choices=["start", "status", "stop"],
        help="Action: start (default), status, stop",
    )
    args = parser.parse_args()
    port = args.port

    if args.action == "status":
        if PID_FILE.exists():
            try:
                info = json.loads(PID_FILE.read_text())
                pid = info.get("pid", 0)
                pid_port = info.get("port", 0)
                try:
                    os.kill(pid, 0)
                    print(f"✅ {SKILL_NAME} API running — PID {pid}, port {pid_port}")
                    for ip in _get_lan_ips():
                        proto = "https" if TLS_ENABLED else "http"
                        print(f"   {proto}://{ip}:{pid_port}")
                except ProcessLookupError:
                    print(f"⚠️  PID file exists but process {pid} is dead (stale)")
                    PID_FILE.unlink(missing_ok=True)
                except PermissionError:
                    print(f"✅ {SKILL_NAME} API running — PID {pid}, port {pid_port}")
            except Exception:
                print(f"⚠️  PID file corrupted")
        else:
            print(f"❌ {SKILL_NAME} API not running")
        return

    if args.action == "stop":
        if PID_FILE.exists():
            try:
                info = json.loads(PID_FILE.read_text())
                pid = info.get("pid", 0)
                if pid:
                    print(f"Stopping {SKILL_NAME} API (PID {pid})...")
                    os.kill(pid, signal.SIGTERM)
                    for _ in range(10):
                        time.sleep(1)
                        try:
                            os.kill(pid, 0)
                        except OSError:
                            print("✅ Stopped")
                            PID_FILE.unlink(missing_ok=True)
                            return
                    print("Force killing...")
                    os.kill(pid, signal.SIGKILL)
                    PID_FILE.unlink(missing_ok=True)
                    print("✅ Force stopped")
                    return
            except ProcessLookupError:
                print("Process not found, cleaning up PID file")
                PID_FILE.unlink(missing_ok=True)
                return
            except Exception as e:
                print(f"❌ Failed to stop: {e}")
                return
        else:
            print(f"❌ {SKILL_NAME} API not running (no PID file)")
        return

    # === start ===
    # 1. 启动期 sanity check：entry / interpreter（不阻断，仅 warn）
    try:
        entry = _resolve_entry()
        logger.info(f"Entry resolved: {entry}")
    except FlashError as e:
        logger.warning(f"Entry check failed: {e.message}")
        logger.warning("Server will still start, but /run will return entry_not_found.")

    interp = INTERPRETER or "python3"
    if shutil.which(interp) is None:
        logger.warning(f"Interpreter '{interp}' not found in PATH; /run will fail.")

    # 2. TLS 证书
    if TLS_ENABLED:
        try:
            _generate_cert()
        except ImportError:
            logger.error("cryptography not installed. Install with: pip install cryptography")
            sys.exit(1)
        except Exception as e:
            logger.error(f"Failed to generate certificate: {e}")
            sys.exit(1)

    schema = "https" if TLS_ENABLED else "http"
    lan_ips = _get_lan_ips()
    print()
    print("=" * 56)
    print(f"  ✨  {SKILL_NAME} API  (flash v2.0)")
    print("=" * 56)
    print(f"  Mode   : subprocess direct execution")
    print(f"  Entry  : {ENTRY_REL or '(unset)'}")
    print(f"  Local  : {schema}://localhost:{port}")
    for ip in lan_ips:
        print(f"  LAN    : {schema}://{ip}:{port}")
    print(f"  Docs   : {schema}://localhost:{port}/docs")
    print(f"  Run    : POST {schema}://localhost:{port}/run")
    print(f"  Timeout: {DEFAULT_TIMEOUT}s (default)")
    print("=" * 56)
    if (args.host in ("0.0.0.0", "::")) and not TLS_ENABLED:
        print()
        print("  ⚠️  监听 0.0.0.0 但 TLS 未启用，调用方走明文 HTTP。仅限信任网络使用。")
    print()

    uvicorn_kwargs = dict(
        app=app,
        host=args.host,
        port=port,
        log_level="info",
        timeout_graceful_shutdown=1,
    )
    if TLS_ENABLED:
        uvicorn_kwargs["ssl_certfile"] = str(CERT_FILE)
        uvicorn_kwargs["ssl_keyfile"] = str(KEY_FILE)
    config = uvicorn.Config(**uvicorn_kwargs)
    server = uvicorn.Server(config)

    global _bound_port
    _bound_port = port
    try:
        server.run()
    except OSError as e:
        if "address already in use" in str(e).lower() or e.errno == 98:
            logger.error(f"Port {port} is already in use. Choose a different port.")
            raise
    finally:
        _clear_pid()


if __name__ == "__main__":
    main()
