def _check_api_key(request: _Request) -> None:
    if not FLASH_API_KEY:
        return
    key = request.headers.get(API_KEY_HEADER_NAME, "").strip()
    # 恒定时间比较，避免 timing attack
    if not hmac.compare_digest(key, FLASH_API_KEY):
        raise HTTPException(
            status_code=401,
            detail="Unauthorized: invalid or missing API key",
        )


def _extract_params(req: RunRequest) -> dict:
    """提取入参（剔除 timeout_seconds）。"""
    return req.model_dump(exclude={"timeout_seconds"})


def _envelope_error(
    error_type: str,
    message: str,
    elapsed_ms: int,
    http_status: int = 500,
) -> JSONResponse:
    return JSONResponse(
        status_code=http_status,
        content={
            "success": False,
            "exit_code": None,
            "elapsed_ms": elapsed_ms,
            "error_type": error_type,
            "error": message,
            "data": None,
            "output": None,
        },
    )


@app.post("/run")
async def run_sync(req: RunRequest, request: _Request):
    """同步执行：subprocess.run + 60s 默认超时 + 512KB 截断 + 统一 envelope。"""
    _check_api_key(request)
    params = _extract_params(req)
    start = time.time()

    # 1. 入参校验
    try:
        _validate_input(params)
    except FlashError as e:
        elapsed = int((time.time() - start) * 1000)
        logger.warning(f"validation_failed: {e.message}")
        return _envelope_error(e.error_type, e.message, elapsed, http_status=400)

    # 2. 构造 argv
    try:
        argv = _build_argv(params)
    except FlashError as e:
        elapsed = int((time.time() - start) * 1000)
        logger.warning(f"{e.error_type}: {e.message}")
        return _envelope_error(
            e.error_type, e.message, elapsed,
            http_status=500 if e.error_type == "entry_not_found" else 400,
        )

    # 3. subprocess 执行
    try:
        proc, stdout_truncated, stderr_truncated = _run_subprocess(
            argv, req.timeout_seconds, truncate=True,
        )
    except FlashError as e:
        elapsed = int((time.time() - start) * 1000)
        logger.error(f"{e.error_type}: {e.message}")
        http_status = 408 if e.error_type == "timeout" else 500
        return _envelope_error(e.error_type, e.message, elapsed, http_status=http_status)
    except Exception as e:
        elapsed = int((time.time() - start) * 1000)
        logger.exception("internal_error during /run")
        return _envelope_error(
            "internal_error",
            f"{type(e).__name__}: {e}",
            elapsed,
            http_status=500,
        )

    elapsed = int((time.time() - start) * 1000)
    envelope = _parse_response(proc, elapsed, stdout_truncated, stderr_truncated)
    logger.info(
        f"/run exit={proc.returncode} elapsed={elapsed}ms "
        f"truncated={envelope.get('truncated', False)}"
    )
    # exit_code != 0 仍返回 200（业务结果非 HTTP 错误，§7.1）
    return envelope


@app.post("/run/async")
async def run_async(req: RunRequest, request: _Request):
    """异步执行：立即返回 job_id；底层用 subprocess；不截断输出。"""
    _check_api_key(request)
    params = _extract_params(req)

    # 入参校验先做（fail-fast）
    try:
        _validate_input(params)
    except FlashError as e:
        return JSONResponse(
            status_code=400,
            content={"status": "failed", "error_type": e.error_type, "error": e.message},
        )

    if not job_store._async_semaphore.acquire(blocking=False):
        return JSONResponse(
            status_code=429,
            content={
                "status": "failed",
                "error_type": "rate_limited",
                "error": f"Server busy: too many concurrent jobs (limit: {job_store._max_async}). Try again later.",
            },
        )

    job_id = job_store.create(SKILL_NAME, params)
    timeout = req.timeout_seconds

    def _execute():
        start = time.time()
        try:
            job_store.update(job_id, status="running")
            argv = _build_argv(params)
            # 异步：不截断
            proc, _, _ = _run_subprocess(argv, timeout, truncate=False)
            elapsed = int((time.time() - start) * 1000)
            envelope = _parse_response(proc, elapsed, False, False)
            status = "completed" if envelope["success"] else "failed"
            job_store.update(
                job_id,
                status=status,
                result=envelope,
                elapsed_ms=elapsed,
            )
            logger.info(f"Async job {job_id} {status} in {elapsed}ms")
        except FlashError as e:
            elapsed = int((time.time() - start) * 1000)
            job_store.update(
                job_id,
                status="failed",
                error=e.message,
                error_type=e.error_type,
                elapsed_ms=elapsed,
            )
            logger.error(f"Async job {job_id} failed [{e.error_type}]: {e.message}")
        except Exception as e:
            elapsed = int((time.time() - start) * 1000)
            job_store.update(
                job_id,
                status="failed",
                error=f"{type(e).__name__}: {e}",
                error_type="internal_error",
                elapsed_ms=elapsed,
            )
            logger.exception(f"Async job {job_id} internal_error")
        finally:
            job_store._async_semaphore.release()

    threading.Thread(target=_execute, daemon=True).start()

    return {
        "job_id": job_id,
        "status": "pending",
        "poll_url": f"/jobs/{job_id}",
    }


@app.get("/jobs/{job_id}")
async def get_job(job_id: str):
    job = job_store.get(job_id)
    if not job:
        raise HTTPException(status_code=404, detail="Job not found or expired (1-hour memory TTL)")
    return job


