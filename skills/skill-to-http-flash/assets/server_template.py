def _build_argv(input_dict: dict[str, Any]) -> list[str]:
    """JSON → CLI argv。

    规则：
      - 字段名 foo_bar → --foo-bar（下划线 → 横杠）
      - 字符串/数字 → ["--key", "value"]
      - True → ["--key"]
      - False / None → 不传
      - list → 重复 flag
      - dict → 序列化为 JSON 字符串
    """
    entry = _resolve_entry()
    interp = INTERPRETER or "python3"
    argv: list[str] = [interp, str(entry)]

    for key, value in input_dict.items():
        if not _FIELD_NAME_RE.match(key):
            raise FlashError(
                "validation_failed",
                f"Invalid parameter name '{key}'. "
                f"Allowed pattern: [a-z][a-z0-9_]* (lowercase, snake_case).",
            )
        flag = f"--{key.replace('_', '-')}"

        if value is None or value is False:
            continue
        if value is True:
            argv.append(flag)
            continue
        if isinstance(value, (str, int, float)):
            argv.extend([flag, str(value)])
            continue
        if isinstance(value, list):
            for item in value:
                if item is None:
                    continue
                if isinstance(item, (dict, list)):
                    argv.extend([flag, json.dumps(item, ensure_ascii=False)])
                else:
                    argv.extend([flag, str(item)])
            continue
        if isinstance(value, dict):
            argv.extend([flag, json.dumps(value, ensure_ascii=False)])
            continue
        # 兜底：转字符串
        argv.extend([flag, str(value)])

    return argv


def _run_subprocess(
    argv: list[str],
    timeout: int,
    truncate: bool = True,
) -> tuple[subprocess.CompletedProcess, bool, bool]:
    """执行 subprocess，返回 (CompletedProcess, stdout_truncated, stderr_truncated)。

    truncate=False 用于异步任务（不截断，完整保留）。
    """
    skill_root = Path(SKILL_DIR).expanduser()
    try:
        proc = subprocess.run(
            argv,
            cwd=str(skill_root),
            capture_output=True,
            timeout=timeout,
            check=False,  # exit_code != 0 不算异常，交给上层 envelope
        )
    except subprocess.TimeoutExpired as e:
        raise FlashError(
            "timeout",
            f"Subprocess timed out after {timeout}s. "
            f"Use POST /run/async for long-running tasks.",
        ) from e
    except FileNotFoundError as e:
        raise FlashError(
            "spawn_failed",
            f"Failed to spawn subprocess: {e}. "
            f"Check that '{argv[0]}' is in PATH.",
        ) from e
    except PermissionError as e:
        raise FlashError(
            "spawn_failed",
            f"Permission denied: {e}",
        ) from e
    except OSError as e:
        raise FlashError(
            "spawn_failed",
            f"OS error spawning subprocess: {e}",
        ) from e

    stdout_truncated = False
    stderr_truncated = False
    if truncate:
        if proc.stdout and len(proc.stdout) > MAX_OUTPUT_BYTES:
            proc.stdout = proc.stdout[:MAX_OUTPUT_BYTES]
            stdout_truncated = True
        if proc.stderr and len(proc.stderr) > MAX_STDERR_BYTES:
            proc.stderr = proc.stderr[:MAX_STDERR_BYTES]
            stderr_truncated = True
    return proc, stdout_truncated, stderr_truncated


def _parse_response(
    proc: subprocess.CompletedProcess,
    elapsed_ms: int,
    stdout_truncated: bool,
    stderr_truncated: bool,
) -> dict[str, Any]:
    """构造统一 envelope。"""
    success = proc.returncode == 0
    stdout_bytes = proc.stdout or b""
    stderr_bytes = proc.stderr or b""
    stdout_str = stdout_bytes.decode("utf-8", errors="replace")
    stderr_str = stderr_bytes.decode("utf-8", errors="replace")

    # 自适应 JSON 解析
    data: Any = None
    output: str | None = None
    if stdout_str.strip():
        try:
            data = json.loads(stdout_str)
        except json.JSONDecodeError:
            output = stdout_str
    else:
        output = ""

    envelope: dict[str, Any] = {
        "success": success,
        "exit_code": proc.returncode,
        "elapsed_ms": elapsed_ms,
        "data": data,
        "output": output,
    }

    # stderr 仅在 success=false 时透出
    if not success:
        envelope["stderr"] = stderr_str if stderr_str else None
        if stderr_truncated:
            envelope["stderr_truncated"] = True

    if stdout_truncated:
        envelope["truncated"] = True
        envelope["hint"] = (
            f"Output exceeded {MAX_OUTPUT_BYTES // 1024}KB. "
            "Use POST /run/async + GET /jobs/<id> for full output."
        )

    return envelope


def _validate_input(input_dict: dict[str, Any]) -> None:
    """JSON Schema 校验，失败抛 FlashError("validation_failed")。

    没装 jsonschema 时降级到 required 检查 + 字段名白名单。
    """
    try:
        import jsonschema  # type: ignore
        try:
            jsonschema.validate(instance=input_dict, schema=INPUT_SCHEMA)
        except jsonschema.ValidationError as e:
            raise FlashError("validation_failed", f"Schema validation failed: {e.message}") from e
    except ImportError:
        # 降级：只检查 required + 字段名
        required = INPUT_SCHEMA.get("required", [])
        for key in required:
            if key not in input_dict or input_dict[key] in (None, ""):
                raise FlashError(
                    "validation_failed",
                    f"Missing required parameter: '{key}'",
                )

    # 额外：字段名白名单
    for key in input_dict:
        if not _FIELD_NAME_RE.match(key):
            raise FlashError(
                "validation_failed",
                f"Invalid parameter name '{key}'. Allowed: [a-z][a-z0-9_]*",
            )


