JOBS_FILE_KEEP = 7


class JobStore:
    """Hybrid in-memory + JSONL job store for async execution."""

    def __init__(self):
        self._jobs: dict[str, dict] = {}
        self._lock = threading.Lock()
        self._file_lock = threading.Lock()
        try:
            _max_async = int(os.environ.get("FLASH_MAX_ASYNC", "20"))
            if _max_async < 1:
                _max_async = 20
        except ValueError:
            _max_async = 20
        self._async_semaphore = threading.Semaphore(_max_async)
        self._max_async = _max_async
        self._start_cleanup()

    def create(self, skill_name: str, params: dict) -> str:
        job_id = uuid.uuid4().hex[:12]
        job = {
            "job_id": job_id,
            "skill": skill_name,
            "status": "pending",
            "params": params,
            "created_at": time.time(),
            "result": None,
            "error": None,
            "elapsed_ms": None,
        }
        with self._lock:
            self._jobs[job_id] = job
        return job_id

    def update(self, job_id: str, **kwargs):
        with self._lock:
            if job_id in self._jobs:
                self._jobs[job_id].update(kwargs)
                if kwargs.get("status") in ("completed", "failed"):
                    self._append_jsonl(self._jobs[job_id])

    def get(self, job_id: str) -> dict | None:
        with self._lock:
            job = self._jobs.get(job_id)
        if job is not None:
            return job
        return self._lookup_jsonl(job_id)

    def _append_jsonl(self, job: dict) -> None:
        with self._file_lock:
            try:
                if JOBS_FILE.exists() and JOBS_FILE.stat().st_size >= JOBS_FILE_MAX_BYTES:
                    self._rotate_jsonl()
                line = json.dumps(job, ensure_ascii=False, default=str)
                with JOBS_FILE.open("a", encoding="utf-8") as f:
                    f.write(line + "\n")
            except Exception as e:
                logger.warning(f"JSONL persist failed for {job.get('job_id')}: {e}")

    def _rotate_jsonl(self) -> None:
        for i in range(JOBS_FILE_KEEP, 0, -1):
            src = JOBS_FILE.with_suffix(f".jsonl.{i - 1}" if i > 1 else ".jsonl")
            dst = JOBS_FILE.with_suffix(f".jsonl.{i}")
            if src.exists():
                if dst.exists():
                    dst.unlink()
                src.rename(dst)

    def _lookup_jsonl(self, job_id: str) -> dict | None:
        if not JOBS_FILE.exists():
            return None
        try:
            with JOBS_FILE.open("r", encoding="utf-8") as f:
                lines = f.readlines()
            for line in reversed(lines):
                line = line.strip()
                if not line:
                    continue
                try:
                    record = json.loads(line)
                    if record.get("job_id") == job_id:
                        return record
                except json.JSONDecodeError:
                    continue
        except Exception as e:
            logger.debug(f"JSONL lookup failed: {e}")
        for i in range(1, JOBS_FILE_KEEP + 1):
            rotated = JOBS_FILE.with_suffix(f".jsonl.{i}")
            if not rotated.exists():
                continue
            try:
                with rotated.open("r", encoding="utf-8") as f:
                    for line in reversed(f.readlines()):
                        line = line.strip()
                        if not line:
                            continue
                        try:
                            record = json.loads(line)
                            if record.get("job_id") == job_id:
                                return record
                        except json.JSONDecodeError:
                            continue
            except Exception:
                continue
        return None

    def _start_cleanup(self):
        def _cleanup():
            while True:
                time.sleep(600)
                cutoff = time.time() - 3600
                with self._lock:
                    expired = [
                        jid for jid, j in self._jobs.items()
                        if j.get("created_at", 0) < cutoff
                        and j.get("status") in ("completed", "failed")
                    ]
                    for jid in expired:
                        del self._jobs[jid]

        t = threading.Thread(target=_cleanup, daemon=True)
        t.start()


